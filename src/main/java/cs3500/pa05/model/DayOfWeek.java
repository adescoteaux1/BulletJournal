package cs3500.pa05.model;

/**
 * represents the names for days of the week
 */
public enum DayOfWeek {

  /**
   * represents monday
   */
  MONDAY,

  /**
   * represents tuesday
   */
  TUESDAY,

  /**
   * represents wednesday
   */
  WEDNESDAY,

  /**
   * represents thursday
   */
  THURSDAY,

  /**
   * represents friday
   */
  FRIDAY,

  /**
   * represents saturday
   */
  SATURDAY,

  /**
   * represents sunday
   */
  SUNDAY
}
